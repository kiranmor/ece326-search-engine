@APP.get("/suggest")
def suggest():
    """
    Autocomplete / search suggestion endpoint.

    - Uses both per-user history and the global Lexicon.
    - Supports multi-word completion by only completing the last token.
    """
    s = request.environ.get("beaker.session")
    user_id = s.get("sub") if s else None
    raw_q = request.query.get("q", "")
    q = raw_q.strip()

    response.content_type = "application/json"
    if not q:
        return json.dumps([])

    # Identify the last token to complete; keep the prefix as-is.
    tokens = q.split()
    last_token = tokens[-1].lower()
    base_prefix = " ".join(tokens[:-1])  # may be empty

    suggestions: list[str] = []
    seen: set[str] = set()
    MAX_TOTAL = 10

    def add_suggestion(text: str) -> None:
        text = (text or "").strip()
        if text and text not in seen:
            suggestions.append(text)
            seen.add(text)

    # 1) Personalized history suggestions
    if user_id:
        for text in recent_queries(user_id):
            candidate = (text or "").strip()
            if not candidate:
                continue
            lc = candidate.lower()

            # Case A: full-query prefix match
            if lc.startswith(q.lower()):
                add_suggestion(candidate)
            else:
                # Case B: use words from this history query to complete last token
                parts_lc = lc.split()
                parts_orig = candidate.split()
                for i, part in enumerate(parts_lc):
                    if part.startswith(last_token):
                        if base_prefix:
                            new_q = base_prefix + " " + parts_orig[i]
                        else:
                            new_q = parts_orig[i]
                        add_suggestion(new_q)
                        break

            if len(suggestions) >= 5:  # cap personalized suggestions
                break

    # 2) Global suggestions from Lexicon based on last_token
    if len(suggestions) < MAX_TOTAL:
        remaining = MAX_TOTAL - len(suggestions)
        for word in global_suggestions(last_token, limit=remaining * 3):
            w = (word or "").strip()
            if not w or not w.lower().startswith(last_token):
                continue

            if base_prefix:
                new_q = base_prefix + " " + w
            else:
                new_q = w

            add_suggestion(new_q)
            if len(suggestions) >= MAX_TOTAL:
                break

    return json.dumps(suggestions)


