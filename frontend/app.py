DB_PATH        = 'history.db'                       # per-user search history

# Shared search database generated by crawler + indexer
SEARCH_DB_PATH = os.path.abspath(
    os.path.join(os.path.dirname(__file__), "..", "frontend", "search.db")
)

PAGE_SIZE = 10   # number of results per page


# --------------------------------------------------------------------
# Per-user history (keep at most 10 recent queries)
# --------------------------------------------------------------------

def init_db() -> None:
    """Ensure the local history database and table exist."""
    with sqlite3.connect(DB_PATH) as con:
        con.execute(
            """
            CREATE TABLE IF NOT EXISTS history (
              user_id TEXT,
              query   TEXT,
              ts      DATETIME DEFAULT CURRENT_TIMESTAMP
            )
            """
        )


def add_query(user_id: str, q: str) -> None:
    """
    Record one query for this user and trim to the 10 most recent
    (by timestamp).
    """
    if not (user_id and q):
        return

    with sqlite3.connect(DB_PATH) as con:
        con.execute(
            "INSERT INTO history(user_id, query) VALUES (?, ?)",
            (user_id, q),
        )
        # Keep only the newest 10 rows for this user
        con.execute(
            """
            DELETE FROM history
            WHERE user_id = ?
              AND rowid NOT IN (
                SELECT rowid
                FROM history
                WHERE user_id = ?
                ORDER BY ts DESC
              )
            """,
            (user_id, user_id),
        )


def recent_queries(user_id: str) -> list[str]:
    """
    Return up to 10 most-recent UNIQUE queries for this user,
    ordered from newest to oldest.
    """
    if not user_id:
        return []

    with sqlite3.connect(DB_PATH) as con:
        cur = con.execute(
            """
            SELECT query
            FROM (
                SELECT query, MAX(ts) AS last_ts
                FROM history
                WHERE user_id = ?
                GROUP BY query
            )
            ORDER BY last_ts DESC
            LIMIT 10
            """,
            (user_id,),
        )
        return [row[0] for row in cur.fetchall()]


def global_suggestions(prefix: str, limit: int = 10) -> list[str]:
    """
    Suggest up to `limit` terms from the global Lexicon table
    (used for autocomplete when user is not logged in).
    """
    prefix = prefix.strip().lower()
    if not prefix:
        return []

    with sqlite3.connect(SEARCH_DB_PATH) as con:
        cur = con.execute(
            """
            SELECT word
            FROM Lexicon
            WHERE lower(word) LIKE ?
            ORDER BY word
            LIMIT ?
            """,
            (prefix + "%", limit),
        )
        return [row[0] for row in cur.fetchall()]


# --------------------------------------------------------------------
# Search core (multi-word query support)
# --------------------------------------------------------------------

def looks_like_url(text: str) -> bool:
    """Heuristic to decide if a string is actually just a URL."""
    if not text:
        return False
    t = text.strip().lower()
    return (
        t.startswith("http://")
        or t.startswith("https://")
        or "://" in t
        or "www." in t
    )


def run_query(q: str) -> list[dict]:
    """
    Execute a multi-word search over the indexed corpus.

    - Split the query into tokens.
    - Look up each token in the Lexicon to get word_ids.
    - Find all documents containing any of those word_ids.
    - Rank documents by:
        (1) how many distinct query words they match (match_count),
        (2) PageRank score (score).
    - Build a list of result dictionaries for the template.
    """
    if not q:
        return []

    # Example: "ece326 lab3 tutorial" -> ["ece326", "lab3", "tutorial"]
    tokens = re.findall(r"\w+", q.lower())
    if not tokens:
        return []

    con = sqlite3.connect(SEARCH_DB_PATH)
    cur = con.cursor()

    # 1) Map query tokens to word_ids via Lexicon
    placeholders = ",".join("?" for _ in tokens)
    cur.execute(
        f"SELECT word_id FROM Lexicon WHERE lower(word) IN ({placeholders})",
        tokens,
    )
    rows = cur.fetchall()
    if not rows:
        con.close()
        return []

    word_ids = [r[0] for r in rows]

    # 2) For all matching word_ids, find docs and count how many
    #    distinct query words each doc contains.
    placeholders = ",".join("?" for _ in word_ids)
    cur.execute(
        f"""
        SELECT doc_id, COUNT(DISTINCT word_id) AS match_count
        FROM InvertedIndex
        WHERE word_id IN ({placeholders})
        GROUP BY doc_id
        """,
        word_ids,
    )
    doc_rows = cur.fetchall()
    if not doc_rows:
        con.close()
        return []

    doc_ids = [r[0] for r in doc_rows]

    # 3) Join Documents + PageRank with the match_count table and sort
    placeholders = ",".join("?" for _ in doc_ids)
    cur.execute(
        f"""
        SELECT d.url, d.title, d.description, p.score, m.match_count
        FROM (
            SELECT doc_id, COUNT(DISTINCT word_id) AS match_count
            FROM InvertedIndex
            WHERE word_id IN ({",".join("?" for _ in word_ids)})
            GROUP BY doc_id
        ) AS m
        JOIN Documents AS d ON d.doc_id = m.doc_id
        JOIN PageRank  AS p ON p.doc_id = m.doc_id
        WHERE d.doc_id IN ({placeholders})
        ORDER BY m.match_count DESC, p.score DESC
        """,
        (*word_ids, *doc_ids),
    )
    rows = cur.fetchall()
    con.close()

    results: list[dict] = []
    for (url, title, desc, score, match_count) in rows:
        parsed = urlparse(url)
        host = parsed.netloc

        # If title is missing or looks like a URL, replace with a generic label.
        if not title or looks_like_url(title) or title.strip() == url.strip():
            if url.lower().endswith(".pdf"):
                title = "PDF"
            else:
                title = "Page"

        # Build a short snippet from description
        desc = desc or ""
        snippet = desc.replace("\n", " ").strip()
        if url in snippet:
            snippet = snippet.replace(url, "").strip()
        if len(snippet) > 200:
            snippet = snippet[:197].rstrip() + "..."

        # Build a short display URL (hostname + first 1–2 segments of path)
        path = parsed.path or ""
        segments = [s for s in path.split("/") if s]
        if segments:
            if len(segments) > 2:
                short_path = "/".join(segments[:2]) + "/..."
            else:
                short_path = "/".join(segments)
            display_url = host + "/" + short_path
        else:
            display_url = host

        results.append(
            {
                "url": url,
                "title": title,
                "display_url": display_url,
                "snippet": snippet,
                "pagerank": float(score),
                "match_count": int(match_count),
            }
        )

    return results


