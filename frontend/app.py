@APP.get("/")
def home():
    """
    Home page.
    - If signed in, show user email and recent searches.
    - If anonymous, show basic search box.
    """
    s = request.environ["beaker.session"]
    email = s.get("email")
    qs = recent_queries(s.get("sub"))
    return template("home", email=email, qs=qs)


@APP.route("/search", method=["GET", "POST"])
def do_search():
    """
    Handle search requests:
    - POST: new query from search box.
    - GET: follow-up requests for pagination.
    """
    s = request.environ["beaker.session"]

    if request.method == "POST":
        raw_q = request.forms.get("q", "").strip()
        page = 1
    else:
        raw_q = request.query.get("q", "").strip()
        try:
            page = int(request.query.get("page", "1") or 1)
        except ValueError:
            page = 1

    if page < 1:
        page = 1

    term = raw_q

    # Record history only for signed-in users and only on the first page
    if s.get("sub") and raw_q and page == 1:
        add_query(s["sub"], raw_q)

    all_results = run_query(term) if term else []

    total = len(all_results)
    total_pages = (total + PAGE_SIZE - 1) // PAGE_SIZE  # ceil division

    if page > total_pages and total_pages > 0:
        page = total_pages

    start = (page - 1) * PAGE_SIZE
    end = start + PAGE_SIZE
    results = all_results[start:end]

    has_next = page < total_pages
    has_prev = page > 1
    pages = list(range(1, total_pages + 1))

    return template(
        "results",
        email=s.get("email"),
        q=term,
        results=results,
        page=page,
        has_next=has_next,
        has_prev=has_prev,
        pages=pages,
        total_pages=total_pages,
    )


@APP.get("/login")
def login():
    """Kick off Google OAuth by redirecting to the consent page."""
    flow = flow_from_clientsecrets(
        CLIENT_SECRETS,
        scope=["profile", "email"],
        redirect_uri=REDIRECT_URI,
    )
    url = flow.step1_get_authorize_url()
    return redirect(url)


@APP.get("/redirect")
def oauth_redirect():
    """
    OAuth callback:
    - Exchange ?code for credentials.
    - Fetch user info.
    - Store email + stable user id ('sub') in session.
    """
    code = request.query.get("code")
    if not code:
        return redirect("/")

    # Exchange 'code' -> credentials (access token + id_token)
    flow = flow_from_clientsecrets(
        CLIENT_SECRETS,
        scope=["profile", "email"],
        redirect_uri=REDIRECT_URI,
    )
    creds = flow.step2_exchange(code)

    # Build authorized client and call Google UserInfo API
    http = httplib2.Http()
    authed = creds.authorize(http)
    users = build("oauth2", "v2", http=authed)
    info = users.userinfo().get().execute()

    s = request.environ["beaker.session"]
    s["email"] = info.get("email")
    s["sub"] = creds.id_token.get("sub") or info.get("id")
    s.save()
    return redirect("/")


@APP.post("/logout")
def logout():
    """Local sign-out: clear the session and return to home."""
    s = request.environ["beaker.session"]
    s.delete()
    return redirect("/")


